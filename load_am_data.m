function [ datamatrix ] = load_am_data( filename )
fid = fopen(filename, 'r');
datamatrix=fread(fid,'uint8=>uint8');
datamatrix=datamatrix((size(datamatrix,1)-768*768*165)+1:size(datamatrix,1));
datamatrix=reshape(datamatrix,768,768,165);
end




